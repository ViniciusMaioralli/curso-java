package br.com.vinicius.cursojava.elementosEstaticos;

public class Matematica {
	
	public static double somar(double n1, double n2) {
		return n1 + n2;
	}
	
	public static double subtrair(double n1, double n2) {
		return n1 - n2;
	}
	
	
}

